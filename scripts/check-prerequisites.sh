#!/bin/bash

# ANSI color codes
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${YELLOW}üîç Checking prerequisites...${NC}"

# Check Java version
if ! command -v java &> /dev/null; then
    echo -e "${RED}‚ùå Java is not installed${NC}"
    exit 1
else
    java_version=$(java -version 2>&1 | awk -F '"' '/version/ {print $2}' | awk -F '.' '{print $1}')
    if [ "$java_version" -lt "23" ]; then
        echo -e "${RED}‚ùå Java 23 or later is required (found version $java_version)${NC}"
        exit 1
    else
        echo -e "${GREEN}‚úÖ Java $java_version is installed${NC}"
    fi
fi

# Check Docker
if ! command -v docker &> /dev/null; then
    echo -e "${RED}‚ùå Docker is not installed${NC}"
    exit 1
else
    echo -e "${GREEN}‚úÖ Docker is installed${NC}"
fi

# Check Docker Compose
if ! command -v docker-compose &> /dev/null; then
    echo -e "${RED}‚ùå Docker Compose is not installed${NC}"
    exit 1
else
    echo -e "${GREEN}‚úÖ Docker Compose is installed${NC}"
fi

# Check Make
if ! command -v make &> /dev/null; then
    echo -e "${RED}‚ùå Make is not installed${NC}"
    exit 1
else
    echo -e "${GREEN}‚úÖ Make is installed${NC}"
fi

# Check if Gradle wrapper exists
if [ ! -f "./gradlew" ]; then
    echo -e "${RED}‚ùå Gradle wrapper not found${NC}"
    exit 1
else
    echo -e "${GREEN}‚úÖ Gradle wrapper is present${NC}"
fi

# Check if Docker daemon is running
if ! docker info &> /dev/null; then
    echo -e "${RED}‚ùå Docker daemon is not running${NC}"
    exit 1
else
    echo -e "${GREEN}‚úÖ Docker daemon is running${NC}"
fi

echo -e "${GREEN}‚úÖ All prerequisites are satisfied!${NC}"